int main(int argc, char **argv)
{
    int fd  = -1;
    int ret = -1;

    /* Open the crypto device */
    fd = open("/dev/rsa", O_RDWR, 0);
    if (fd < 0)
    {
        perror("open(/dev/rsa)");
        return -1;
    }

    ret = ST_DoRsa(fd);
    if (0 != ret)
    {
        printf("ST_DoRsa Fail\n");
        close(fd);
        return -1;
    }

    /* Close the original descriptor */
    if (close(fd))
    {
        perror("close(fd)");
        return -1;
    }

    return 0;
}
