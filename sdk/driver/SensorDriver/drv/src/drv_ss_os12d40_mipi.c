            handle->pCus_sensor_init = pCus_init_mipi4lane_2m30fps_linear;
            params->expo.Max_vts = RES_2m30_vts;
            Preview_line_period = PREVIEW_LINE_PERIOD(RES_2m30_vts, 30);
            Mirror_reg_01 = 0x01;
            Mirror_reg_02 = 0x0d;
