static int pCus_SetAEGain(ss_cus_sensor *handle, u32 gain)
{
    //extern DBG_ITEM Dbg_Items[DBG_TAG_MAX];
    os12d40_params *params = (os12d40_params *)handle->private_data;
    u32 input_gain = 0, dig_gain = 0, a_gain = gain;
    u16 gain16 = 0;

    SENSOR_DMSG("\n\n[%s], gain %u \n", __FUNCTION__,gain);

    if (gain < 1024)
        gain = 1024;
    else if (gain >= SENSOR_MAX_GAIN)
        gain = SENSOR_MAX_GAIN;

    input_gain = gain;
    if(a_gain < 1024)
        a_gain = 1024;
    else if(a_gain >= SENSOR_MAX_A_GAIN)
    {
        a_gain = SENSOR_MAX_A_GAIN;
        dig_gain = ((gain * 1024)/a_gain);
    }

    gain16=(u16)(a_gain >> 2);
    params->tGain_reg[0].data = (gain16 >> 8) & 0x2f;//high bit
    params->tGain_reg[1].data = gain16 & 0xfe; //low byte

    if(dig_gain < 1024)
        dig_gain = 1024;

    params->tGain_reg[2].data = (dig_gain >> 10) & 0xf;
    params->tGain_reg[3].data = (dig_gain >> 2) & 0xff;
    params->tGain_reg[4].data = (dig_gain << 6)& 0xc0;

    SENSOR_DMSG("\n\n[%s], gain %u, dig_gain %u, a_gain %u \n", __FUNCTION__,gain, dig_gain, a_gain);
    params->dirty = true;
    return SUCCESS;

}


