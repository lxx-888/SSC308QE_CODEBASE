static int pCus_init_mipi4lane_11m30fps_linear(ss_cus_sensor *handle)
{
    int i,cnt = 0;
    os12d40_params *params = (os12d40_params *)handle->private_data;
    SENSOR_DMSG("\n[%s]", __FUNCTION__);

    for(i = 0; i< ARRAY_SIZE(Sensor_init_table_4lane_11m30fps_linear); i++)
    {
        if(Sensor_init_table_4lane_11m30fps_linear[i].reg==0xffff)
        {
            SENSOR_MSLEEP(Sensor_init_table_4lane_11m30fps_linear[i].data);
        }
        else
        {
            cnt = 0;
            while(SensorReg_Write(Sensor_init_table_4lane_11m30fps_linear[i].reg,Sensor_init_table_4lane_11m30fps_linear[i].data) != SUCCESS)
            {
                cnt++;
                if(cnt >= 10)
                {
                    return FAIL;
                }
            }
        }
    }
    SensorReg_Read(0x3811, &params->h_win_offset);
    SensorReg_Read(0x3813, &params->v_win_offset);
    return SUCCESS;
}

